const starting = ["(", "[", "{", "<"]
const ending = [")", "]", "}", ">"]
const score = [3, 57, 1197, 25137]

function check(line) {
    let stack = []
    for (const character of line) {
        if (starting.includes(character)) {
            stack.push(starting.indexOf(character))
        }
        if (ending.includes(character)) {
            const start = stack.pop()
            const end = ending.indexOf(character)
            if (start !== end) {
                log(`${line.join('')} - Expected ${ending[start]}, but found ${ending[end]} instead.`)
                return score[end]
            }
        }
    }
    return 0
}

function completionScore(line) {
    let stack = []
    for (const character of line) {
        if (starting.includes(character)) {
            stack.push(starting.indexOf(character))
        }
        if (ending.includes(character)) {
            const start = stack.pop()
            const end = ending.indexOf(character)
            if (start !== end) {
                //log(`${line.join('')} - Expected ${ending[start]}, but found ${ending[end]} instead.`)
                return 0
            }
        }
    }
    const score = stack.reduceRight((acc, n) => acc * 5 + n + 1, 0)
    log(`${line.join('')} - Complete by adding  ${stack.map(n => ending[n]).reverse().join('')} - ${score} total points.`)
    return score
}

function compute(tab) {
    let total = 0;
    for (const line of tab) {
        total += check(line)
    }
    return total;
}

function compute2(tab) {
    let score = [];
    for (const line of tab) {
        const completion = completionScore(line)
        if (completion > 0) {
            score.push(completion)
        }
    }
    
    score.sort((a,b)=> a - b)
    log(score)
    return score[~~(score.length / 2)];
}

