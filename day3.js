function downToboggan(tab, left = 3, down = 1) {
    let x = 0;
    let y = 0;
    let tree = 0
    while(y < tab.length) {
        if (tab[y][x] === '#') {
            tree++;
        }
        x = (x + left) % tab[y].length;
        y += down;
    }
    return tree;
}

