let version = process.argv[2] || 0

function createTab(input) {
  return input.split('\n').map(line => line.split(''))
}

function createListAsteroid(map) {
  let list = []
  for (let x = 0; x < map.length; x++)
    for (let y = 0; y < map[x].length; y++)
      if (map[x][y] === '#')
        list.push({x, y})
  return list;
}

